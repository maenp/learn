-- 运算符

-- 1. 算术运算符
print(10 + 20) -- 加法
print(10 - 20) -- 减法
print(10 * 20) -- 乘法
print(10 / 20) -- 除法
print(10 % 3) --  取模
print(5 ^ 2) --   幂
print(10 // 20) -- 取整除

-- 2. 比较运算符
print(10 == 20) -- 等于     false
print(10 ~= 20) -- 不等于   true
print(10 < 20)  -- 小于     true
print(10 > 20)  -- 大于     false
print(10 <= 20) -- 小于等于 true
print(10 >= 20) -- 大于等于 false

-- 3. 逻辑运算符
print(true and false) -- 与     false
print(true or false)  -- 或     true
print(not true)       -- 非     false

-- 4. 其他运算符
print(10 .. 20) -- 连接 1020
print(#"Hello") -- 长度 5       一元运算符，返回字符串或表的长度。